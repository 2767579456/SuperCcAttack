public class StatisticInfo
{
    /// <summary>
    /// 总请求数
    /// </summary>
    public long TotalRequest = 0;

    /// <summary>
    /// 总请求成功数
    /// </summary>
    public long SuccessRequest = 0;

    /// <summary>
    /// 总请求失败数
    /// </summary>
    public long FailRequest = 0;

    /// <summary>
    /// 总请求数据量
    /// </summary>
    public long RequestBytes = 0;

    /// <summary>
    /// 当前请求数
    /// </summary>
    public long CurrentRequest = 0;
}
